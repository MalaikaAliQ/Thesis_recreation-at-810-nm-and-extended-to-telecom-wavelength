"""
=============================================================================
REAL-VALUE 810 nm CHSH Weak-Measurement Simulation
PPKTP + Calcite + SPAD detector correction
=============================================================================

This code replaces the idealised 100% detector / lossless-crystal assumptions
with real material parameters supplied from:
  - PPKTP/KTP datasheet values
  - Calcite optical constants from RefractiveIndex.INFO screenshots
  - Thesis detector efficiency value

Major non-ideal corrections:
  1. Detector efficiency = 33% (thesis value requested by user)
  2. PPKTP/KTP Sellmeier refractive indices included
  3. PPKTP length, aperture, flatness, absorption, coating, transparency,
     angular acceptance, temperature acceptance, bandwidth, walk-off, and
     nonlinear coefficient included
  4. Calcite ordinary Sellmeier equation included
  5. Calcite Fresnel reflection loss included wavelength-dependently
  6. Calcite birefringence, GVD, group index, dispersion, and Brewster angle
     included in visibility/count-rate model
  7. Coincidence counts and statistical uncertainty are computed from detected
     photon pairs, not from ideal generated pairs.
=============================================================================
"""

