# ── helpers ───────────────────────────────────────────────
def gauss(x, mu, sig):
    return np.exp(-(x-mu)**2/(2*sig**2)) / (sig*np.sqrt(2*np.pi))

def show(fig):
    display(fig)
    plt.close(fig)

