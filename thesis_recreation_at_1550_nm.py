def sim_corr(corr, n):
    a_out  = np.random.choice([-1, 1], size=n)
    p_same = (1 + abs(corr)) / 2
    same   = np.random.choice([True, False], size=n, p=[p_same, 1-p_same])
    b_out  = np.where(same, np.sign(corr)*a_out, -np.sign(corr)*a_out)
    return np.mean(a_out * b_out)

