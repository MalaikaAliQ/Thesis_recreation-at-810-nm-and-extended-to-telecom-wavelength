import qutip as qt
import numpy as np
import matplotlib.pyplot as plt

# ====================== UPDATED PARAMETERS (More realistic weak regime) ======================
N = 120                          # Pointer Hilbert space dimension
sigma = 1.0                      # Initial pointer width (standard)
g1 = 0.40                        # First weak measurement coupling (smaller = weaker)
g2 = 0.40                        # Second weak measurement coupling (non-commuting)

x = np.linspace(-10, 10, 800)

# Pointer operators
a = qt.destroy(N)
P = 1j * (a.dag() - a) / np.sqrt(2)

pointer0 = qt.coherent(N, 0)

# System observables (non-commuting)
sz = qt.sigmaz()   # e.g., H/V basis
sx = qt.sigmax()   # e.g., diagonal basis (+45° / -45°)

# Initial system state (superposition, typical for Bell test)
psi_system = (qt.basis(2, 0) + qt.basis(2, 1)).unit()   # |+x⟩ state

def pointer_prob(state):
    rho_ptr = state.ptrace(1)
    probs = np.abs([qt.expect(qt.ket2dm(qt.basis(N, i)), rho_ptr) for i in range(N)])
    probs /= np.sum(probs) * (x[1] - x[0] + 1e-10)
    return np.interp(x, np.linspace(-10, 10, N), probs)

# ====================== SIMULATION ======================
plt.figure(figsize=(16, 9))

total0 = qt.tensor(psi_system, pointer0)
p0 = pointer_prob(total0)
plt.plot(x, p0, 'k--', lw=3.2, label='Initial pointer distribution')

