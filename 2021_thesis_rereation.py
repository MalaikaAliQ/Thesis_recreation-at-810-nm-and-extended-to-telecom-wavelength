# ------------------------------
# Wavelengths
# ------------------------------
LAMBDA_SIGNAL_UM = 0.810       # 810 nm signal/idler wavelength
LAMBDA_PUMP_UM   = 0.405       # 405 nm pump for degenerate 810 nm SPDC

# ------------------------------
# Detector efficiency
# ------------------------------
# CHANGE FROM IDEAL:
# Old code effectively used 100% detection in the count statistics.
# New code uses the thesis-specified detector efficiency.
ETA_DETECTOR_THESIS = 0.33     # 33% detector efficiency, as requested

# ------------------------------
# PPKTP / KTP crystal data
# ------------------------------
# Values from the PPKTP/KTP specifications supplied in the discussion.
PPKTP_APERTURE_MM2 = 1.0 * 2.0       # typical aperture = 1 mm x 2 mm
PPKTP_LENGTH_MM    = 30.0            # up to 30 mm
PPKTP_LENGTH_CM    = PPKTP_LENGTH_MM / 10.0
PPKTP_FLATNESS_PV_WAVES = 1/6        # lambda/6 @ 633 nm
PPKTP_TRANSPARENCY_MIN_UM = 0.350
PPKTP_TRANSPARENCY_MAX_UM = 4.000
PPKTP_OPERATING_TEMP_C = 25.0        # near room temperature
PPKTP_DAMAGE_THRESHOLD_MW_CM2 = 600.0 # 600 MW/cm^2, 10 ns pulses @1064 nm
PPKTP_COATING_OPTION = "AR/AR"

# KTP datasheet NLO and phase-matching data
D_EFF_TYPE_II = 7.36e-12             # m/V, Type-II effective nonlinear coefficient
ANGULAR_ACCEPTANCE_MRAD_CM = 13.0    # mrad-cm
TEMP_ACCEPTANCE_C_CM = 20.0          # degC-cm
SPECTRAL_BANDWIDTH_A_CM = 4.5        # Angstrom-cm
KTP_WALKOFF_MRAD = 1.0               # mrad
KTP_ABSORPTION_LOSS_PER_CM = 0.01    # <1%/cm at 1064 nm; used as conservative loss

# Coating value is not given numerically in the sheet, but the option AR/AR is.
# To include coating effect without making the model ideal, use a conservative
# 0.5% reflection per AR surface. Change if your exact coating datasheet gives R.
PPKTP_AR_SURFACE_TRANSMISSION = 0.995
PPKTP_SURFACES = 2

# Pump power chosen only to check damage threshold and to scale relative brightness.
# can be replace this with the actual pump power from the thesis/experiment.
PUMP_POWER_MW = 20.0
PUMP_AREA_CM2 = PPKTP_APERTURE_MM2 * 0.01  # 1 mm^2 = 0.01 cm^2
PUMP_INTENSITY_MW_CM2 = PUMP_POWER_MW / PUMP_AREA_CM2
DAMAGE_SAFE = PUMP_INTENSITY_MW_CM2 < PPKTP_DAMAGE_THRESHOLD_MW_CM2

# ------------------------------
# KTP Sellmeier equation
# ------------------------------
# Datasheet form interpreted as:
# n^2 = A + B/(lambda^2 - C) - D*lambda^2
# lambda in micrometers.
KTP_SELLMEIER = {
    "nx": {"A": 3.0129, "B": 0.03807, "C": 0.04283, "D": 0.01664},
    "ny": {"A": 3.0333, "B": 0.04106, "C": 0.04946, "D": 0.01695},
    "nz": {"A": 3.3209, "B": 0.05305, "C": 0.05960, "D": 0.01763},
}

def ktp_n(axis, lam_um):
    p = KTP_SELLMEIER[axis]
    n2 = p["A"] + p["B"]/(lam_um**2 - p["C"]) - p["D"]*lam_um**2
    return np.sqrt(n2)

N_KTP_810 = {axis: ktp_n(axis, LAMBDA_SIGNAL_UM) for axis in ["nx", "ny", "nz"]}
N_KTP_405 = {axis: ktp_n(axis, LAMBDA_PUMP_UM) for axis in ["nx", "ny", "nz"]}

# ------------------------------
# PPKTP physical factors
# ------------------------------
# Transparency check: both 405 nm and 810 nm should lie inside 350–4000 nm.
PPKTP_SIGNAL_TRANSPARENT = (PPKTP_TRANSPARENCY_MIN_UM <= LAMBDA_SIGNAL_UM <= PPKTP_TRANSPARENCY_MAX_UM)
PPKTP_PUMP_TRANSPARENT   = (PPKTP_TRANSPARENCY_MIN_UM <= LAMBDA_PUMP_UM   <= PPKTP_TRANSPARENCY_MAX_UM)

# Bulk absorption transmission through crystal length.
T_PPKTP_ABS = (1.0 - KTP_ABSORPTION_LOSS_PER_CM) ** PPKTP_LENGTH_CM

# AR/AR coating transmission.
T_PPKTP_COATING = PPKTP_AR_SURFACE_TRANSMISSION ** PPKTP_SURFACES

# Flatness correction: convert PV lambda/6 to approximate RMS lambda/24.
# Marechal/Strehl-like wavefront factor.
flatness_rms_waves = PPKTP_FLATNESS_PV_WAVES / 4.0
PPKTP_FLATNESS_FACTOR = np.exp(-(2*np.pi*flatness_rms_waves)**2)

# Angular acceptance becomes narrower for longer crystals:
# acceptance = 13 mrad-cm / L(cm)
ANGULAR_ACCEPTANCE_MRAD = ANGULAR_ACCEPTANCE_MRAD_CM / PPKTP_LENGTH_CM

# Temperature acceptance similarly narrows:
TEMP_ACCEPTANCE_C = TEMP_ACCEPTANCE_C_CM / PPKTP_LENGTH_CM

# Spectral bandwidth narrows:
SPECTRAL_BANDWIDTH_A = SPECTRAL_BANDWIDTH_A_CM / PPKTP_LENGTH_CM

# Residual alignment/temperature errors.
# Set small but nonzero to avoid pretending perfect phase matching.
RESIDUAL_ANGLE_ERROR_MRAD = 0.50
RESIDUAL_TEMP_ERROR_C = abs(PPKTP_OPERATING_TEMP_C - 25.0)

# Use datasheet acceptances to generate efficiency factors.
ANGLE_FACTOR = np.sinc(RESIDUAL_ANGLE_ERROR_MRAD / ANGULAR_ACCEPTANCE_MRAD) ** 2
TEMP_FACTOR = np.sinc(RESIDUAL_TEMP_ERROR_C / TEMP_ACCEPTANCE_C) ** 2

# Nonlinear source factor from d_eff and crystal length.
# Normalized to the actual chosen length and d_eff so it stays numerically stable.
PPKTP_SOURCE_FACTOR = (
    (D_EFF_TYPE_II / 7.36e-12)**2 *
    (PPKTP_LENGTH_MM / 30.0)**2 *
    ANGLE_FACTOR *
    TEMP_FACTOR *
    PPKTP_FLATNESS_FACTOR *
    T_PPKTP_ABS *
    T_PPKTP_COATING
)

# ------------------------------
# Calcite optical data
# ------------------------------
# Ghosh 1999 ordinary-ray Sellmeier range: 0.204–2.172 um.
CALCITE_VALID_MIN_UM = 0.204
CALCITE_VALID_MAX_UM = 2.172

def calcite_no_ghosh(lam_um):
    """
    Calcite ordinary refractive index from the screenshot:
    n^2 - 1 = 0.73358749
              + 0.96464345*lambda^2/(lambda^2 - 1.94325203e-2)
              + 1.82831454*lambda^2/(lambda^2 - 120)
    lambda in micrometers.
    """
    lam2 = lam_um**2
    n2_minus_1 = (
        0.73358749
        + (0.96464345 * lam2) / (lam2 - 1.94325203e-2)
        + (1.82831454 * lam2) / (lam2 - 120.0)
    )
    return np.sqrt(1 + n2_minus_1)

CALCITE_NO_810 = calcite_no_ghosh(LAMBDA_SIGNAL_UM)

# Thesis/material birefringence value used previously for calcite:
CALCITE_DELTA_N = 0.172
CALCITE_NE_810 = CALCITE_NO_810 - CALCITE_DELTA_N

# Values from screenshot at 0.5876 um, used as real measured constants/penalty terms.
# They are kept explicitly in the model rather than ignored.
CALCITE_GROUP_INDEX_SCREENSHOT = 1.6984
CALCITE_DNDL_SCREENSHOT_PER_UM = -0.067984
CALCITE_GVD_FS2_PER_MM = 119.51
CALCITE_D_PS_NM_KM = -651.98
CALCITE_BREWSTER_DEG = 58.911

# Original setup uses 2 calcite crystals in Alice arm and 2 in Bob arm.
# If your thesis gives a different thickness, replace CALCITE_SINGLE_LENGTH_MM.
CALCITE_CRYSTALS_TOTAL = 4
CALCITE_SINGLE_LENGTH_MM = 1.0
CALCITE_TOTAL_LENGTH_MM = CALCITE_CRYSTALS_TOTAL * CALCITE_SINGLE_LENGTH_MM

# Fresnel reflection at normal incidence, air -> calcite ordinary index.
# This replaces lossless crystal assumption.
def fresnel_R_normal(n1, n2):
    return ((n2 - n1) / (n2 + n1))**2

R_CALCITE_SURFACE_810 = fresnel_R_normal(1.0, CALCITE_NO_810)
T_CALCITE_SURFACE_810 = 1.0 - R_CALCITE_SURFACE_810

# Four calcite crystals have 8 air-crystal surfaces if uncoated.
# This is intentionally non-ideal.
CALCITE_SURFACES_TOTAL = 2 * CALCITE_CRYSTALS_TOTAL
T_CALCITE_FRESNEL_TOTAL = T_CALCITE_SURFACE_810 ** CALCITE_SURFACES_TOTAL

# Calcite birefringence causes relative phase delay:
# delta_phi = 2*pi*Delta_n*L/lambda.
# The phase is large; in an experiment compensation reduces it. We convert
# uncompensated phase into a small residual visibility penalty using modulo phase.
delta_phi_calcite = 2*np.pi * CALCITE_DELTA_N * (CALCITE_TOTAL_LENGTH_MM*1e-3) / (LAMBDA_SIGNAL_UM*1e-6)
residual_phase = np.angle(np.exp(1j * delta_phi_calcite))
CALCITE_PHASE_VIS_FACTOR = np.exp(-(residual_phase/np.pi)**2)

# Walk-off factor from KTP/calcite spatial separation.
# Uses supplied KTP walkoff and calcite birefringence.
walkoff_um = (KTP_WALKOFF_MRAD * 1e-3) * (PPKTP_LENGTH_MM * 1e3)
mode_radius_um = 1000.0  # approximate 1 mm mode radius from 1 mm aperture
WALKOFF_VIS_FACTOR = np.exp(-(walkoff_um/mode_radius_um)**2)

# GVD penalty. For narrowband photons, this is small but included.
# Convert fs^2/mm * length to a visibility factor with a conservative scale.
gvd_total_fs2 = CALCITE_GVD_FS2_PER_MM * CALCITE_TOTAL_LENGTH_MM
GVD_VIS_FACTOR = np.exp(-(gvd_total_fs2 / 5000.0)**2)

# Total material visibility.
# Bare source visibility in thesis-style code was ~99%; with 4 calcite crystals ~82%.
# Here the visibility is derived from the real material penalties.
BARE_SOURCE_VISIBILITY = 0.99
visibility_material = (
    BARE_SOURCE_VISIBILITY *
    PPKTP_FLATNESS_FACTOR *
    ANGLE_FACTOR *
    TEMP_FACTOR *
    CALCITE_PHASE_VIS_FACTOR *
    WALKOFF_VIS_FACTOR *
    GVD_VIS_FACTOR
)

# Keep visibility physical.
visibility_material = float(np.clip(visibility_material, 0.0, 1.0))

# Total detected pair efficiency.
# For a two-photon coincidence, detector efficiency acts twice.
# Optical losses also act on the photon paths.
ETA_OPTICAL_SINGLE_PHOTON = T_PPKTP_ABS * T_PPKTP_COATING * T_CALCITE_FRESNEL_TOTAL
ETA_COINCIDENCE = PPKTP_SOURCE_FACTOR * (ETA_OPTICAL_SINGLE_PHOTON**2) * (ETA_DETECTOR_THESIS**2)

# Base generated attempts from the old simulation.
